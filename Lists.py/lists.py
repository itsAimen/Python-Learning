# Lists allows us to work with a collection of data in sequential order
# List values are placed in between square brackets [ ], separated by commas. It is good practice to put a space between the comma and the next value. 
# The values in a list do not need to be unique (the same value can be repeated)
# Empty lists do not contain any values within the square brackets 

odd_numbers = [11, 21, 31, 61, 71]
print(odd_numbers)

# A list can contain anything; a string, integer, boolean, and float

names = ["Aimen, Ashar"]
print(names)

names_ages = ["Aimen", 21, "Ashar", 2]
print(names_ages)

