# If statement is used to determine the excecution of code based on the evaluation of a Boolean expression
# If the if statement expression evaluates to 'True', then the indented code following the statement is executed
# If the expression evaluates to 'False' then the indented code following hte if statement is skipped and the program executes the next line of code which is indented at the same level as the if statement

# if Statement

user_name = "Aimen"
if user_name == "Aimen":
    print("Welcome!")

money = 5
if money < 10:
    print("You owe me $50!")

flower = "sunflower"
if flower == "rose":
    print("I like roses too")

# this if statement does not execute the print command because the statement is 'False' 