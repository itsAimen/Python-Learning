# Equal Operator == is used to compare two values, variables or expression to determine if they are the same
# If the value being compared are the same, the operator returns 'True', otherwise it returns 'False'
# The operator takes the data type into account when making the comparison, so a string value of "2" is not considered the same as a numeric value of 2

statement1 = 2 * 2 == 2 + 2
statement2 = 15 - 3 == 2 * 6
statement3 = 4 / 2 == 3

print(statement1)
print(statement2)
print(statement3)
