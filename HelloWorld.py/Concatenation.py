# Besides using the + character to add two numbers together, it can also be used to add two strings together.
# The process of adding two strings together is called 'String Concatenation'
# content from multiple strings can be combined into one string however, the individual strings are added without any space. For that we need to add " " for spacing while joining strings together. 

String1 = "Hello!"
String2 = "My name is Ume Aimen."
String3 = "I am currently learning Python."

print(String1 + " " + String2 + " " + String3)

# Or

message = (String1 + " " + String2 + " " + String3)

print(message)