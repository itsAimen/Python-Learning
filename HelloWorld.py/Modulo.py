# The modulo operator is indicated by % and gives the remainder of a division calculation.

# The result of the expression 4 % 2 would result in the value 0,
# because 4 is evenly divisible by 2. Leaving no remainder
# The result of the expression 7 % 3 would return 1, 
# because 7 is not evenly divisible by 3, leaving a remainder of 1

calculation1 = 4 % 2
calculation2 = 7 % 3

print(calculation1)
print(calculation2)

